    public ExceptionFrame(Throwable t){
        this();

        String text = "Critical Error! Please post the following stack trace " +
                "at the forums:\n\n";

        text += t.toString() + "\n";

        for(StackTraceElement e : t.getStackTrace())
            text += e.toString() + "\n";

        jTextArea1.setText(text);
    }
